import numpy as np

def calculate_loss(conf_matrix):
    # Your confusion matrix
    cm = np.array(conf_matrix)

    # Compute correct predictions (sum of diagonal)
    correct = np.trace(cm)

    # Compute total predictions
    total = np.sum(cm)

    # Zero-one loss (misclassification rate)
    zero_one_loss = 1 - (correct / total)

    # Print results
    print(f"Zero-One Loss: {zero_one_loss:.4f}; Correct Predictions: {correct}; Total Predictions: {total}; Accuracy: {correct / total:.4f}")
    return zero_one_loss

